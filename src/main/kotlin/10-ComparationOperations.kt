fun main() {
//  Comparation Operations is operation that produce boolean value
//  consist of <, >, <=, >=, ==, !=
    val a = 10
    val b = 20
    val res = a < b
    val res2 = a > b
    val res3 = a == b
    val res4 = a != b
    println(res)
    println(res2)
    println(res3)
    println(res4)

}