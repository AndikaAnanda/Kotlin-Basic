fun main() {

//  Integer number (usually using int) :
    var a: Byte = 2
    var b: Int = 100
    var c: Short = 10
    var d: Long = 10000000000000L
//  kita dapat memberi underscore sebagai pengganti tanda titik agar angka yang nilainya besar lebih jelas
    var e: Long = 1_000_000_000L

//  Floating Point number (usually using double) :
    var a1: Float = 2.78F
    var a2: Double = 345.7

//  Literals Number (using Int) :
    var decimalNumber: Int = 100
    var hexadecimalNumber: Int = 0xFF
    var binaryNumber: Int = 0b10101010
}